import Foundation
import UIKit

extension UIFont {
    class func ProximaNovaRegular(size: CGFloat) -> UIFont? {
        return UIFont(name: "ProximaNova-Regular", size: size)
    }
    
    class func ProximaNovaBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "ProximaNova-Bold", size: size)
    }
}
